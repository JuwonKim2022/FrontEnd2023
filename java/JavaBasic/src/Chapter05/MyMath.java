package Chapter05;

public class MyMath {
    long add(long a, long b){
        long result = a+b;
        return result;
    }
    long substract(long a, long b){
        long result = a-b;
        return result;
    }
    long multiply(long a, long b){
        long result = a*b;
        return result;
    }
    long divide(long a, long b){
        long result = a/b;
        return result;
    }
}
